import discord
from discord.ext import commands
# –ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –≤–µ–±-—Å–∞–π—Ç–∞–º–∏
import requests
# –ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—É—á–µ–Ω–Ω—ã—Ö —Å –≤–µ–±-—Å–∞–π—Ç–∞
from bs4 import BeautifulSoup
# –ë–∏–±–ª–∏–æ—Ç–µ–∫–∞ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ —Å–ª—É—á–∞–π–Ω—ã—Ö –∑–Ω–∞—á–µ–Ω–∏–π
import random

# –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—è –±–æ—Ç–∞
config = {
    # –£–Ω–∏–∫–∞–ª—å–Ω—ã–π –∫–ª—é—á –¥–æ—Å—Ç—É–ø–∞ –∫ —Å–æ–∑–¥–∞–Ω–Ω–æ–º—É –±–æ—Ç—É
    'token': 'TOKEN',
    # –ü—Ä–µ—Ñ–∏–∫—Å –¥–ª—è –≤—ã–∑–æ–≤–∞ –∫–æ–º–º–∞–Ω–¥ –±–æ—Ç–∞
    'prefix': '!',
}

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Å–ª—É—á–∞–π–Ω–æ–π –Ω–æ–≤–æ—Å—Ç–∏ —Å —Å–∞–π—Ç–∞ –†–ò–ê
async def random_news():
    # –ú—ã –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø—Ä–æ—Å –Ω–∞ —Å–∞–π—Ç –∏ –ø–æ–ª—É—á–∞–µ–º –æ—Ç–≤–µ—Ç –æ—Ç –Ω–µ–≥–æ
    response = requests.get('https://ria.ru/')
    # –û—Ç–≤–µ—Ç –æ—Ç —Å–∞–π—Ç–∞ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å python
    soup = BeautifulSoup(response.text, 'html.parser')
    # –í –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–Ω–æ–º —Ñ–∞–π–ª–µ –∏—â–µ–º –Ω–æ–≤–æ—Å—Ç–∏
    news = soup.findAll(class_='cell-list__item-link')
    # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –∫–ª—é—á –¥–ª—è —Å–ª—É—á–∞–π–Ω–æ–π –Ω–æ–≤–æ—Å—Ç–∏
    rand = random.randint(0, len(news) - 1)
    # –ü–µ—Ä–µ–¥–∞–µ–º —Å–ª—É—á–∞–π–Ω—É—é –Ω–æ–≤–æ—Å—Ç—å
    return news[rand].text, news[rand]['href']

# –§—É–Ω–∫—Ü–∏—è –ø–æ–ª—É—á–µ–Ω–∏—è —Å—Ç–∞—Ç–µ–π —Å –í–∏–∫–∏–ø–µ–¥–∏–∏
async def wiki_info(request):
    # –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –∑–∞–ø—Ä–æ—Å –Ω–∞ —Å–∞–π—Ç, –∑–∞–º–µ–Ω—è—è –ø—Ä–æ–±–µ–ª—ã –Ω–∏–∂–Ω–∏–º –ø–æ–¥—á–µ—Ä–∫–∏–≤–∞–Ω–∏–µ–º
    a = request.replace(" ", "_")
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø—Ä–æ—Å –Ω–∞ –≤–∏–∫–∏–ø–µ–¥–∏—é, –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ–º –æ—Ç–≤–µ—Ç
    resp = requests.get(f'https://ru.wikipedia.org/wiki/{a}')
    # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –æ—Ç–≤–µ—Ç –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å python
    soup = BeautifulSoup(resp.text, 'html.parser')
    # –ò—â–µ–º —Ç–µ–∫—Å—Ç –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –ø–æ –∑–∞–ø—Ä–æ—Å—É
    wiki_text = soup.find('p').text
    # –°–æ–±–∏—Ä–∞–µ–º –≤—Å–µ "—Å–º–µ–∂–Ω—ã–µ" —Å—Å—ã–ª–∫–∏ –∏–∑ —Å—Ç–∞—Ç—å–∏
    another_wiki = soup.find_all(class_='mw-redirect')
    # –ü–æ–ª—É—á–∞–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –∫ —Å—Ç–∞—Ç—å–µ
    wiki_img = soup.find(class_='thumbinner').find('img')
    # –ï—Å–ª–∏ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –µ—Å—Ç—å - –ø–µ—Ä–µ–¥–∞–µ–º –Ω–∞–±–æ—Ä –¥–∞–Ω–Ω—ã—Ö (—Ç–µ–∫—Å—Ç —Å—Ç–∞—Ç—å–∏, —Å—Å—ã–ª–∫–∞ –Ω–∞ —Å—Ç–∞—Ç—å—é, —Å—Å—ã–ª–∫–∏ –Ω–∞ "—Å–º–µ–∂–Ω—ã–µ" —Å—Ç–∞—Ç—å–∏, —Å—Å—ã–ª–∫–∞ –Ω–∞ –∫–∞—Ä—Ç–∏–Ω–∫—É –∫ —Å—Ç–∞—Ç—å–µ)
    if wiki_img != None:
        return wiki_text, f'https://ru.wikipedia.org/wiki/{a}', another_wiki, wiki_img['src']
    # –ò–Ω–∞—á–µ –ø–µ—Ä–µ–¥–∞–µ–º –Ω–∞–±–æ—Ä –¥–∞–Ω–Ω—ã—Ö (—Ç–µ–∫—Å—Ç —Å—Ç–∞—Ç—å–∏, —Å—Å—ã–ª–∫–∞ –Ω–∞ —Å—Ç–∞—Ç—å—é, —Å—Å—ã–ª–∫–∏ –Ω–∞ "—Å–º–µ–∂–Ω—ã–µ" —Å—Ç–∞—Ç—å–∏)
    else:
        return wiki_text, f'https://ru.wikipedia.org/wiki/{a}', another_wiki

